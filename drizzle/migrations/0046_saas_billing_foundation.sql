  KEY `idx_saas_admin_audit_actor` (`actorUserId`),
  KEY `idx_saas_admin_audit_entity` (`entityType`, `entityId`),
  KEY `idx_saas_admin_audit_created` (`createdAt`),
  CONSTRAINT `fk_saas_admin_audit_tenant` FOREIGN KEY (`tenantId`) REFERENCES `tenants` (`id`) ON DELETE SET NULL,
  CONSTRAINT `fk_saas_admin_audit_actor` FOREIGN KEY (`actorUserId`) REFERENCES `users` (`id`) ON DELETE SET NULL
);
